function getSeimeiRank(stroke) {
  const n = stroke > 81 ? (stroke % 81 === 0 ? 81 : stroke % 81) : stroke;
  return seimei81[n] || seimei81[1];
}

// 画数の末尾から五行を取得
function strokeToGogyo(stroke) {
  const last = stroke % 10;
  if (last === 1 || last === 2) return "木";
  if (last === 3 || last === 4) return "火";
  if (last === 5 || last === 6) return "土";
  if (last === 7 || last === 8) return "金";
  return "水";
}

// 五行相生・相剋判定
function seimeiGogyoRelation(a, b) {
  const seisei = { "木": "火", "火": "土", "土": "金", "金": "水", "水": "木" };
  const sokoku = { "木": "土", "土": "水", "水": "火", "火": "金", "金": "木" };
  if (a === b) return "比和";
  if (seisei[a] === b) return "相生";
  if (sokoku[a] === b) return "相剋";
  return "無関係";
}

